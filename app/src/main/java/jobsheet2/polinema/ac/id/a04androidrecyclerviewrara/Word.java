package jobsheet2.polinema.ac.id.a04androidrecyclerviewrara;

public class Word {
    private String word;
    private String no;

    public Word(String word, String no){
        this.word = word;
        this.no = no;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
